# Para ello debe poder agregar un alumno, eliminarlo, modificar sus datos y listar todos los alumnos.
# Los alumnos guardarán su nombre, apellido, edad y las notas de 5 materias.
# Las materias deberán almacenarse en un diccionario, donde la clave será el nombre de la materia y el valor será una lista con una nota por cada trimestre.
# El programa debe permitir calcular el promedio total de cada alumno y formar automáticamente el pódio de los mejores alumnos para la bandera.
# Siendo el mejor promedio el de la bandera de la Argentina, el segundo Entre Ríos, el tercero de Concordia, luego los primeros escolatas, y luego los segundos escoltas.
